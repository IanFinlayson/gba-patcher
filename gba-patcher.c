int main(int argc, char** argv) {
    if (argc != 2) {
        printf("Usage:\ngba-patcher file.gba\n");
        return -1;
    }
    
    FILE* romfile = fopen(argv[1], "r+");
    if (romfile == NULL) {
        printf("Could not open rom file '%s' for patching.\n", argv[1]);
        return -1;
    }


    /* seek to byte 4 where the bitmap needs to go */
    fseek(romfile, 4, SEEK_SET);

    /* fill in the bitmap byte by byte */
    for (int i = 0; i < BITMAP_SIZE; i++) {
        fputc(bitmap[i], romfile);
    }

    /* now we put in the title of the game */
    char title[12] = "HOMEBREWGAME";
    for (int i = 0; i < 12; i++) {
        fputc(title[i], romfile);
    }

    /* next comes a 4 byte game code
     * A - normal game
     * HB - game title abbreviation
     * E - english language */
    char game_code[4] = "AHBE";
    for (int i = 0; i < 4; i++) {
        fputc(game_code[i], romfile);
    }

    /* next comes a 2 byte maker code, not sure if matters, 01 is Nintendo */
    fputc('0', romfile);
    fputc('1', romfile);

    /* the next byte is required to be 0x96 */
    fputc(0x96, romfile);

    /* the next 10 bytes can/should be 0 */
    for (int i = 0; i < 10; i++) {
        fputc(0, romfile);
    }

    /* finally we have a header checksum byte
     * this was manually calculated based on the information above
     * so DON'T change the title or game code else the checksum will be wrong!
     * The calculation is done using the following algorithm
     *
     * check = 0
     * foreach byte from the start of the title through the magix 0x96 byte:
     *      check -= byte
     * check -= 0x19
     * check &= 0xff
     *
     * I don't know why they do it with the subtraction this way
     *
     */
    fputc(0x6d, romfile);

    /* the file should be good to go now! */
    fclose(romfile);

    return 0;
}

